numero1 = int(input("Ingrese un número distinto de cero: "))
numero2 = int(input("Ingrese otro número distinto de cero: "))

print(f"{numero1} + {numero2} = {numero1 + numero2}")
print(f"{numero1} / {numero2} = {numero1 / numero2}")
print(f"{numero1} * {numero2} = {numero1 * numero2}")
print(f"{numero1} - {numero2} = {numero1 - numero2}")
